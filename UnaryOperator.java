public class UnaryOperator {
    public static void main(String args[]){
        int a=5;
        int b=3;
        System.out.println("before calculation the value of a is "+a);
        System.out.println("before calculation the value of b is "+b);
        System.out.println(a-- - --b);
        System.out.println("After calculation the value of a is "+a);
        System.out.println("After calculation the value of a is "+b);
    }
}
